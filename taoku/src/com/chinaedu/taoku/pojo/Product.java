package com.chinaedu.taoku.pojo;

public class Product {
	Integer pid;
	String  pname;
	Integer pprice;
	Integer ppricezk;
	String pimg;
	String pdes;
	Integer pcata;
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public Integer getPprice() {
		return pprice;
	}
	public void setPprice(Integer pprice) {
		this.pprice = pprice;
	}
	public Integer getPpricezk() {
		return ppricezk;
	}
	public void setPpricezk(Integer ppricezk) {
		this.ppricezk = ppricezk;
	}
	public String getPimg() {
		return pimg;
	}
	public void setPimg(String pimg) {
		this.pimg = pimg;
	}
	public String getPdes() {
		return pdes;
	}
	public void setPdes(String pdes) {
		this.pdes = pdes;
	}
	public Integer getPcata() {
		return pcata;
	}
	public void setPcata(Integer pcata) {
		this.pcata = pcata;
	}
	
	
}
